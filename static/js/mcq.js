var selectedDiv = "";
var x = document.getElementsByClassName('box-first')
for (var i = 0; i < x.length; i++) {
    x[i].addEventListener("click", function(){
        
    var selectedEl = document.querySelector(".selected-first");
    if(selectedEl){
        selectedEl.classList.remove("selected-first");
    }
    this.classList.add("selected-first");
        
    }, false);
}
